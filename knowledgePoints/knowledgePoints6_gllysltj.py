#   什么是概率论与数理统计
#       必然现象中的确定性规律
#           例
#               1+1=2
#               自由落体运动公式h=1/2gt^2
#       自然界还是社会生活中还大量存在着随机现象
#           例
#               人的寿命
#               天气现象
#               金融市场
#       随机现象虽然存在不确定性，但还是有某些规律的
#           例
#               老年人的余寿一般来说比年轻人短
#               明年6月1日有很大可能杭州比北京的气温要高
#               抛一枚硬币出现正反面的可能性是一样的
#           这种规律就叫统计规律
#       概率统计是各类学科中唯一一门专门研究随机现象的规律性的学科
#       随机现象的广泛性决定了这一学科的重要性
