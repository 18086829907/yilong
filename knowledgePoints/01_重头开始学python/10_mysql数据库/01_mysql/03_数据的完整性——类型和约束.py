#表的验证
#   目的：保证存储数据的准确性
#   类别
#       数据类型验证
#       约束验证


#数据类型  # 选择数据类型原则：够用就行，尽量选择取值范围小的，这样更节约空间
#   整数：int, bit
#   小数：decimal(5,2)  # 共存5位，小数占2位
#   字符串
#       varchar：可变长  # 例varchar(3)，输入'ab'，不足3位变长度存储，即'ab'，超过3位不存储
#       char：固定长度  # 例char(3)，输入'ab'，不足3位末尾插入空格保证3位，即'ab '，超过3位不能存储
#       text：大文本  # 字符大于4000时，推荐使用
#   日期时间：date, tiem, datetime
#   枚举类型：enum('男','女')
#   图片、视频、音频：数据库不存二进制数据，而是存素材当电脑中的路径
#       推荐使用七牛云存储，将素材存到云上，七牛会提供素材存储路径，将这些路径存到本地数据库即可

#类型及范围
