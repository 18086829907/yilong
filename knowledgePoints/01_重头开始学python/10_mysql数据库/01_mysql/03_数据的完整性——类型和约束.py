#约束
#   主键：primary key  # 物理上存储的顺序
#   无符号：unsigned  # 规定数字类型为无负号的符号
#   非空：not null  # 此字段不允许填写空值
#   唯一：unique  # 此字段的值不允许重复
#   默认：default  # 当不填写此值时会使用默认值，如果填写时以填写为准
#   外键：foreign key  # 存储另外表的主键的字段，我们称为外键。设定为外键的字段中的值必须存在于另外一个表中的主键，否则不允许插入。
#       说明
#           如果另外表中的主键太多，会造成性能下降，因为外键会到另外表主键中去遍历判断值是否存在于此。能不能外键约束，则不用
#           为了保证外键字段的值的有效性，一般从逻辑层进行控制，即你的代码
