#           词根是关键词的最小单位
#           词根主要提升关键词权重
#       词根如何提升关键词权重
#           例
#               标题：特大号塑料收纳箱衣服整理箱加厚清仓大号收纳盒有盖衣物储物箱子
#               词根：收纳盒、整理箱、衣服
#               说明
#                   如果关键词权重为300，则三个词根会平分权重，即收纳盒100、整理箱199、衣服100
#                   所有包含这3个词根的其他关键词也就有了相应权重
#                       塑料收纳盒
#                           关键词权重100分
#                               塑料和收纳盒都获得权重，且权重之后等于100，且塑料权重低于收纳盒
#                       衣服收纳盒
#                           关键词权重为衣服100分+收纳盒100分=200分权重
#                       衣服整理箱
#                           关键词权重为衣服100分+整理箱100分=200分权重
#                       大号收纳盒
#                           关键词权重100分
#                               大号和收纳盒都获得权重，且权重之后等于100，且塑料权重低于收纳盒
#                       ...
#               总结：都有权重的词根放在一起，权重累加；无权重和有权重的词放在一起，权重共享，且本来具有权重的词根权重高
#               经验：包含有权重词根的所有关键词就有了权重，因此做好一个宝贝，展现词越来越多的原因
