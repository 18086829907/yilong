#【day17】多种定价思路
#一口价和促销价
#   一口价改动影响很大
#   折扣 = 实际售价/真是售价
#   促销价改动不响应
#       5-7折最好
#           单品宝
#           名不虚传（怎么实现打折后的价格?）
#   定价指的是实际售价（促销价）
#       一口价设置是根据折扣度
#           70实际
#               5折 一口价定价140
#               7折 一口价定价100
#定价的因素
#   定价目标
#       引流款
#           这类产品就是用来引流
#               市场占有率最大
#                   高性价比
#       利润款
#           当期利润最大化
#               消化引流款引进店铺的流量，定价参考引流款主词所在的价格区间。利润款一旦自身的搜索流量大于分流的流量，要做适当的价格调整
#       活动款
#           维持生存
#       品质款/形象款
#           这类产品的特点就是质量好，价格高，销量少。
#               满足高端顾客需求
#   成本
#       固定成本
#           员工固定工资、房租
#       商品成本
#           进价（或生产成本）
#       变动成本
#           提成、推广费、物流等
#       销售利润表
#           网盘下载
#   市场需求
#       市场需求是定价的高限
#       要求价格弹性对定价的影响
#           富有弹性
#               允许降价扩大销量
#                   薄利多销
#                   大多数产品
#           缺乏弹性
#               稳定价格或者提价
#               独特商品
#               品牌产品
#               刚需产品
#   竞争
#       竞争对手的产品和价格影响定价
#           同款竞品价格
#           同档次同类价格
#       一般竞争越大、价格越低
#           价格战
#定价的基本策略
#   折扣定价策略
#       现金折扣
#           全场5折
#       数量折扣
#           买一送一
#       功能折扣
#           两个档99，产品升级后三个档99
#       季节折扣
#   地区定价
#       江浙沪包邮
#   心理定价
#       尾数定价
#           3.1.1 9.9
#           3.1.2 8.8
#   锚点定价
#       多SKU定价
#       对比定价
#           一家连锁店的小型面包机售价1399元，后来又出了一个大型机售价2399，结果大型机直销，小型机销量翻番
#       大原则
