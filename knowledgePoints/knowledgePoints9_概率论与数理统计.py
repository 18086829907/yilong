#【day】概率
#定义1
#   概率的统计性定义：
#       当试验的次数增加时，随机事件A发生的频率的稳定值p称为概率，记为P(A) = p
#定义2
#   概率的公理化定义：
#       设随机试验对应的样本空间为S，对每个事件A，定义P(A)，满足：
#           非负性：P(A)>=0    性质1
#           规范性：P(S)=1    性质2
#           可列可加性：A1,A2...两两互斥，即AiAj=Φ，i!=j,则
#               n         n
#           P(  ∪  Ai) = ∑  P(Ai) 即 P(A1∪A2∪A3...∪An) = P(A1)+P(A2)+P(A3)...+P(An)    性质3
#               i=1      i=1
#   满足三条公里的P(A)称为事件A的概率
#   不可能事件概率的性质（由公理化定义推导出）
#       P(Φ)=0
#       P(A)=1-P(^-A)
#       有限可加性
#           A1,A2,...An, AiAj=Φ，i!=j
#                   n         n
#               P(  ∪  Ai) = ∑  P(Ai) 即 P(A1∪A2∪A3...∪A∞) = P(A1)+P(A2)+P(A3)...+P(A∞)
#                   i=1      i=1
#概率的其他性质
#   若A⊂B，则有P(B-A)=P(B) - P(A)    #B发生A不发生的概率=B发生的概率 - A发生的概率
#       证
#           由图得到B=A∪(B-A)，根据概率的性质
#               P(B)=P(A∪(B-A))=P(A)+P(B-A)
#               => P(B-A)=P(B)-P(A)    性质4
#               => p(B)>=P(A)
#               更特殊的性质 P(A)<=P(S)=1
#   若AB相交,即AB!=Φ,
#       由图得 B - A = B - AB
#           P(B-A) = P(B-AB)
#           因为 AB的始终包含于B
#           所有 P(B-A) = P(B)-P(AB)    性质5
#概率的加法公式
#   P(A∪B) = P(A) + P(B) - P(AB)
#   证:
#       由图得 A∪B=A∪(B-A)
#           => P(A∪B) = P(A∪B-A) = P(A)+P(B-A) = P(A)+P(B)-P(AB)
#           说明：第一步-代入，第二步-概率的可列可加性，第三步-概率的性质5
#   说明：由图可以直观看出P(A)+P(B),其实将P(AB)加了两次，减掉之后就是P(A∪B)
#   推广1
#       P(A∪B∪C) = P(A)+P(B)+P(C)-P(AB)-P(AC)-P(BC)+P(ABC)
#       证：
#           P(A∪B∪C) = P(A∪B) + P(C) - P((A∪B)C) = P(A∪B) + P(C) - P(AC∪BC) = P(A) + P(B) - P(AB) + P(C) - (P(AC) + P(BC) - P(ACBC))
#               => P(A∪B∪C) = P(A) + P(B) + P(C) - P(AB) - P(AC) - P(BC) + P(ACBC)
#               => P(A∪B∪C) = P(A) + P(B) + P(C) - P(AB) - P(AC) - P(BC) + P(ABC)
