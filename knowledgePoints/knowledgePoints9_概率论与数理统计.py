#【day】样本空间与随机事件
#自然界与社会生活中的两类现象
#   确定性现象
#       在一定条件下必然发生的现象
#           例如：在一个标准大气压下，水加热到100摄氏度一定会沸腾
#   随机现象
#       在一定条件下具有多种可能结果，且试验时无法预知出现哪个结果的现象
#           例如：抛掷骰子可能出现‘1点’，也可能是其他情况
#           例如：检验产品可能是合格品，也可能是不合格品
#   例题判断下列现象属于随机现象还是确定现象
#       向上抛出的物体会下落
#       打吧，击中靶心
#       买了彩票会中奖
#   随机现象在实验的过程中出现的结果不确定，但随着实验的次数增大，又会呈现出一定规律性，而概率论与数理统计就是研究随机现象规律性的学科
#   对随机现象的观察、记录、实验统称为随机试验，它具有以下特点
#       可以在相同条件下重复进行
#       事先知道所有可能出现的结果
#       进行试验前并不知道哪个试验结果会发生
#       例，下面的随机试验中都具备随机试验特征
#           抛一枚硬币，观察试验结果
#           对某路公交车某停靠站登记下车人数
#           对听课人数进行一次登记
#样本空间
#   定义：随机试验的所有可能结果构成的集合称为样本空间，记为S={e},S中的元素e称为样本点
#   写出下列例题的样本空间
#       一枚硬币抛一次：S={'正‘，’反‘}
#       一个城市一天中会发生多少次事故：S={0,1,2,3,4,5,...50,51,52....}
#       记录一批产品的寿命x：S={x:x>=0} 即 S={x for i in range(0, \d*)}
#       记录某地一昼夜最高温低x，最低温度y：S={(x,y):a<=y<=x<=b}
#随机事件
#   事件A发生
#       样本空间S的子集A称为随机事件A，简称事件A。当且仅当A中的某个样本点发生称事件A发生
#   事件的表示
#       事件A的表示可以用集合，也可用语言来表示
#           例:观察某公交站的候车人数，样本空间S={x:x>=0};事件A表示’至少有5人候车‘，A={x:x>=5}，事件B表示'候车人数不多于2人'，B={x:0<=x<=2}
#   必然事件
#       如果把S看作事件，则每次试验S总是发生，所以S称为必然事件
#   基本事件
#       如果事件只含有一个样本点，称其为基本事件
#           例：观察某公交站的候车人数，样本空间S={x:x>=0};事件C表示’恰好有3人等车‘，事件C={3}，C是一个基本事件
#   不可能事件
#       如果事件时空集，里面不包含任何样本点，记为Φ，则每次试验Φ都不发送，称Φ为不可能事件
#           例：观察某公交站的候车人数，样本空间S={x:x>=0};事件D表示’候车人数既少于3个又多于3‘，事件C={x:x<3 and x>3}={x:None}，D是一个不可能事件

#【day】事件的相互关系及运算
#例：甲乙两人进行掷骰子比赛，得点数大者为胜，若甲先投得了5点，分析乙胜负情况
#   S={'事件A':{'胜':{6}}, '事件B':{'平':{5}}, '事件C':{'败':{1,2,3,4}}}
#   发生'乙不输'事件，即A与B的合并组成{5,6}
#事件关系
#   包含
#       A⊂B:事件A发生一定导致B发送
#   相等
#       A=B
#           A⊂B
#           B⊂A
#       判断AB的关系
#           例题
#               A={'明天天晴':{'晴','多云'}},B={明天无雨:{'晴','多云','阴'}} => A⊂B
#               A={'有多于4人候车':{x:x>4}},B={'至少有5人候车':{x:x>=5}} => A=B
#               一枚硬币投两次，A={'第一次是正面':{'正':{1,2}},B={'至少有一次正面':{'正':{1,2}} => A=B
#   和事件，记为A∪B
#       A∪B={x|x∈A 或 x∈B},A与B至少有一发生
#   积事件，记为A∩B,A●B,AB
#       A∩B={x|x∈A 且 x∈B},A与B同时发生
#           A={1,3,5},B={1,2,3},A∩B={1,3}
#       推广到有限个事件的和事件以及积事件
#           和事件发生
#               n
#               ∪Ai = A1∪A2∪A3...∪An ->  表示A1,A2,A3...An中至少有一个发生，称他们的和事件发生
#               i=1
#           积事件发生
#               n
#               ∩Ai = A1∩A2∩A3...∩An ->  表示A1,A2,A3...An同时发生，称他们的积事件发生
#               i=1
#   互斥
#       AB=Φ，称事件A与B不相容或互斥
#   A与B的差事件
#       A-B={x|x∈A且x∉B}
#           A={1,3,5},B={1,2,3},A-B={5},B-A={2}
#   A的逆事件，记为^-A,也称A的互逆、对立事件，由于电脑无法输入逆符号则用^-代替，逆符号仅是A上面的一个类似减号的横线符号
#       A∪^-A=S,A^-A=Φ，^-^-A=A
#   A与B的差事件可以表示为
#       A-B=A^-B=A∪B-B=A-AB
#事件的运算定律
#   交换律：A∪B=B∪A,AB=B∩A
#   结合律：A∪(B∪C)=(A∪B)∪C,A∩(B∩C)=(A∩B)∩C
#   分配律：A∪(B∩C)=(A∪B)∩(A∪C),A∩(B∪C)=(A∩B)∪(A∩C)
#   对偶率：^-A^-∪^-B=^-A∩^-B, ^-A^-∩^-B=^-A∪^-B,
#       对偶定律推广
#                 n       n
#               ^-∪Ai =  ∩ ^-Ai = ^-A1∩^-A2∩^-A3...∩^-An
#                 i=1     i=1
#                 n       n
#               ^-∩Ai =   ∪ ^-Ai = ^-A1∪^-A2∪^-A3...∪^-An
#                 i=1     i=1
#        对偶定律常常用于对于一个概率计算有困难时，计算其逆事件的概率往往有奇效
#   图解随机事件是否一致
#       (A∪B)-C!=A∪(B-C)
#   注意^-(AB)与^-A^-B的区别
#       ^-(AB)表示A、B不同时发生
#       ^-A^-B表示A、B都不发生
#       实际上两者有关系：
#           ^-(AB) = ^-A^-B ∪ A^-B ∪ ^-AB
#   例题
#       设A={'甲来听课'},B={'乙来听课'}，则：
#           S={'都不来','甲不来','乙不来','甲乙都不来'} = {'都不来','甲来','乙来','甲乙都来'} = {'都不来','甲不来','乙不来','甲乙都来'}
#           A∪B = {'甲乙至少有一人来听课'} = {'甲来','乙来','甲乙都来'}
#           A∩B = {'甲乙都来听课'}
#           ^-(A∪B) = ^-A^-B = {'甲乙都没来'}
#           ^-A∪^-B = ^-(AB) = {'甲乙至少有一人不来'} = {'都不来','甲不来','乙不来'} = {'甲乙最多有一人来'} = {'甲来','乙来','都不来'}
#   例题
#       用ABC三个事件关系及运算表示下列各事件
#           A发生，B、C都不发生
#               A^-B^-C = A-B-C
#           恰有一个发生
#               A^-B^-C∪^-AB^-C∪^-A^-BC
#           至少有一个发生
#               A∪B∪C = ^-(^-A^-B^-C) = (A^-B^-C∪^-AB^-C∪^-A^-BC)∪(^-ABC∪A^-BC∪AB^-C)∪(ABC)
#           至少有两个发生
#               AB∪AC∪BC = AB^-C∪A^-BC∪^-ABC
#           至少有一个不发生
#               ^-A∪^-B∪^-C = ^-(ABC) = ^-ABC∪A^-BC∪AB^-C∪^-A^-BC∪A^-B^-C∪^-AB^-C∪^-A^-B^-C

