#必然现象中的确定性规律：
#   1+1=2
#   自由落体运动公式h=1/2gt^2
#自然界还是社会生活中还大量存在随机现象：
#   随机现象存在不确定性
#       人的寿命
#           谁都无法预知我们自己能活几年
#       天气现象
#           到底是阴还是晴，我们不能确定
#       金融市场
#           银行汇率、股票市场都无法确定
#       抛掷硬币
#           出现正面和背面的结果不确定
#   随机现象虽然存在不确定性，但还是有某些规律的
#       人的寿命
#           老年人的预售一般来说比年轻人短
#       天气现象
#           杭州的天气一般来说比北京气温要高
#       抛掷硬币
#           抛一枚均匀硬币出现正反面的可能性是一样的
#   统计规律
#       出现在随机现象中的规律
#   概率统计是各类学科中唯一一门专门研究随机现象的规律性的学科
#   随机现象的广泛性决定了这一学科的重要性
#   确切来说，概率论与数理统计是两个学科
#       概率论
#           是数学的一个分支，研究如何定量描述随机现象及其规律
#       数理统计
#           则以数据为唯一研究对象，包括数据的收集、整理、分析和建模，从而对随机现象的某些规律进行预测或决策
#           大数据时代的来临，更为统计的发展带来了极大的机遇和挑战

